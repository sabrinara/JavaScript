//add element in array
//number push
var numbers=[0,10,20,30,50];
numbers.push(100);
console.log(numbers);

// string push
var friends =['jomila','sormila','jodu','modu'];
friends.push('akkash');
console.log(friends);

//delete element from array
//number pop
var numbers=[0,10,20,30,50];
numbers.pop();
console.log(numbers);

// string pop
var friends =['jomila','sormila','jodu','modu'];
friends.pop();
console.log(friends);

//push in first index
var numbers=[0,10,20,30,50];
numbers.unshift(100);
console.log(numbers);

//pop in first index
var numbers=[0,10,20,30,50];
numbers.shift();
console.log(numbers);